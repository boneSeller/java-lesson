package Lesson2;

public class PositiveNotPositive {
    public static void main(String[] args) {
        postiveNotPositive(32);
    }

    public static void postiveNotPositive(int a) {
        if (a >= 0) {
            System.out.println("Positive");
        } else {
            System.out.println("Negative");
        }
    }
}
